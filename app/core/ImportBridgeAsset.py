class CantDeterminTypeError(Exception):
    def __init__(self, type:str) -> None:
        super().__init__(f"type :{type} not supported")

def DeterminMapTypeByString(s:str) -> ut.AssetMapType:
    if s.lower() == 'ao':
        return ut.AssetMapType.AO
    elif  s.lower() == 'albedo':
        return ut.AssetMapType.Albedo
    elif  s.lower() == 'normal':
        return ut.AssetMapType.Normal
    elif  s.lower() == 'roughness':
        return ut.AssetMapType.Roughness
    elif  s.lower() == 'displacement':
        return ut.AssetMapType.Displacement
    elif s.lower() == 'cavity':
        return ut.AssetMapType.Cavity
    elif s.lower() == 'gloss':
        return ut.AssetMapType.Gloss
    elif s.lower() == 'curvature':
        return ut.AssetMapType.Curvature
    elif s.lower() == 'specular':
        return ut.AssetMapType.Specular
    elif s.lower() == 'bump':
        return ut.AssetMapType.Bump
    elif s.lower() == 'fuzz':
        return ut.AssetMapType.Fuzz
    elif s.lower() == 'opacity':
        return ut.AssetMapType.Opacity
    elif s.lower() == 'translucency':
        return ut.AssetMapType.Translucency
    elif s.lower() == 'transmission':
        return ut.AssetMapType.Transmission
    elif s.lower() == 'diffuse':
        return ut.AssetMapType.Diffuse
    elif s.lower() == 'metalness':
        return ut.AssetMapType.Metalness
    elif s.lower() == 'mask':
        return ut.AssetMapType.Mask
    elif s.lower() == 'brush':
        return ut.AssetMapType.Brush
    elif s.lower() == 'normalobject':
        return False
    elif s.lower() == 'thickness':
        return ut.AssetMapType.Thickness
    elif s.lower() == 'normalbump':
        return False
    else:
        raise CantDeterminTypeError(s)

