        ut.AddAssetDataToDataBase(asset)


        print(f"Name:{asset.name}\nType:{asset.type}\nCategory:{asset.category}\nSubCategory:{asset.subcategory}\nTags:{asset.tags}\nLod Count:{len(asset.Lods)}")
        for map in asset.assetMaterials[0].maps:
            print(map.uri)
        db.insert(dict(id=assetData["id"]))
        print("##########")

