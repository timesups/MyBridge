def ImportBridgeAssetFromDir(rootPath:str):
    rootJsonFilePath = os.path.join(rootPath,"assetsData.json")
    if not os.path.exists(rootJsonFilePath):
        return False
    with open(rootJsonFilePath,'r',encoding='utf-8') as f:
        assetsData = json.loads(f.read())
    db = TinyDB("ImportedAssetsID.txt")
    for i in range(len(assetsData)):
        print("##########")
        print(f"index:{i}")
        assetData = assetsData[i]

        user = Query()
        result = db.search(user.id == assetData['id'])
        if result != []:
            print("current asset is imported")
            continue

        assetRootPath = os.path.join(rootPath,"\\".join(assetData['path']))
        print(f"Asset source path:{assetRootPath}")
        jsPath = os.path.join(rootPath,"\\".join(assetData['jsonPath']))
        if not os.path.exists(jsPath):
            print("asset json file is not exists")
            return False
        with open(jsPath,'r',encoding='utf-8') as f:
            assetDetail = json.loads(f.read())

        asset = ut.Asset()
        asset.type = DeterminAssetTypeByString(assetDetail['categories'][0])
        # 跳过不支持的类型
        if not asset.type:
            print("asset type is not supported!")
            continue
        asset.name = assetDetail['name']
        asset.tags = assetDetail['tags']
        lodNormals = {}
        basecolor_flag = False
        material = ut.Material()
        material.name = asset.name
        if 'maps' in assetDetail.keys():
            for map in assetDetail['maps']:
                mapuri = os.path.join(assetRootPath,map['uri'])
                if not os.path.exists(mapuri):
                    continue
                assetMap = ut.AssetMap()
                assetMap.type = DeterminMapTypeByString(map["type"])
                if not assetMap.type:
                    continue
                assetMap.uri = mapuri
                assetMap.name,assetMap.extension = os.path.splitext(map['uri'])
                assetMap.subMapCount = 1
                assetMap.UDIM = False
                assetMap.size = ut.GetTextureSize(assetMap.uri)
                if not assetMap.size:
                    continue
                if assetMap.type == ut.AssetMapType.Albedo:
                    basecolor_flag = True
                material.maps.append(assetMap)
        elif 'components' in assetDetail.keys():
            for component in assetDetail['components']:
                maps = []
                # 循环获取所有存在的贴图
                for resolutions in component['uris'][0]['resolutions']:
                    for image in resolutions["formats"]:
                        if os.path.exists(os.path.join(assetRootPath,image["uri"])):
                            image = copy.deepcopy(image)
                            image['type'] = component["type"]
                            maps.append(image)
                #遍历所有获得的贴图
                for map in maps:
                    map_uri = os.path.join(assetRootPath,map["uri"])
                    #判断是否是LOD贴图

                    assetMap = ut.AssetMap()
                    assetMap.type = DeterminMapTypeByString(map["type"])
                    if not assetMap.type:
                        continue
                    assetMap.uri = map_uri
                    assetMap.name,assetMap.extension = os.path.splitext(os.path.basename(map_uri))
                    assetMap.subMapCount = 1
                    assetMap.UDIM = False
                    assetMap.size = ut.GetTextureSize(assetMap.uri)
                    if not assetMap.size:
                        continue
                    if assetMap.type == ut.AssetMapType.Albedo:
                        basecolor_flag = True
                    #处理贴图为lod法线的情况
                    if "lod" in map.values():
                        lod_level = ut.GetLodLevelByName(assetMap.uri)
                        lodNormals[lod_level] = assetMap
                        continue
                    material.maps.append(assetMap)
        else:
            raise
        new_maps = {}
        for map in material.maps:
            key = f"{map.type.value}_{map.extension}"
            if key not in new_maps.keys():
                new_maps[key] = map
            else:
                if map.size > new_maps[key].size:
                    new_maps[key] = map
        material.maps = list(new_maps.values())
        asset.assetMaterials.append(material)


        if not basecolor_flag and asset.type != ut.AssetType.Brush:
            print("当前模型缺失颜色贴图,跳过")
            print(jsPath)
            continue
        #开始处理模型部分
        if asset.type == ut.AssetType.Assets3D or asset.type == asset.type == ut.AssetType.Plant:
            if "meshes" in assetDetail.keys():
                for mesh in assetDetail['meshes']:

                    for i in range(len(mesh['uris'])):
                        mesh_uri = os.path.join(assetRootPath,mesh['uris'][i]['uri'])
                        if os.path.exists(mesh_uri):
                            break
                    else:
                        #当循环正常结束,说明没有满足的模型文件,所以跳过当前mesh 的循环
                        continue
                    if mesh['type'] == "original":
                        asset.OriginMesh == ut.AssetMesh()
                        asset.OriginMesh.uri = mesh_uri
                        asset.OriginMesh.name,asset.OriginMesh.extension = os.path.splitext(os.path.basename(asset.OriginMesh.uri))
                    elif mesh['type'] == "lod":
                        lod = ut.LOD()
                        lod.mesh = ut.AssetMesh()
                        lod.mesh.uri = mesh_uri
                        lod.mesh.name,lod.mesh.extension = os.path.splitext(os.path.basename(lod.mesh.uri))
                        lod.level = ut.GetLodLevelByName(lod.mesh.name)
                        if lod.level in lodNormals.keys():
                            lod.normalMap = lodNormals[lod.level]
                        asset.Lods.append(lod)
                    elif mesh['type'] == "zbrush":
                        asset.ZbrushFile = mesh_uri
                    else:
                        raise
                if asset.OriginMesh.uri == "":
                    asset.OriginMesh.uri = asset.Lods[0].mesh.uri
                    asset.OriginMesh.name,asset.OriginMesh.extension = os.path.splitext(os.path.basename(asset.OriginMesh.uri))
                    asset.assetMaterials[0].maps.append(asset.Lods[0].normalMap)
            elif "models" in assetDetail.keys():
                meshVars = {}
                for mesh in assetDetail['models']:
                    mesh_uri = os.path.join(assetRootPath,mesh['uri'])
                    if not os.path.exists(mesh_uri):
                        #跳过不存在的模型
                        continue
                    if mesh['variation'] == 1:
                        if mesh['lod'] == 0:
                            asset.OriginMesh = ut.AssetMesh()
                            asset.OriginMesh.uri = mesh_uri
                            asset.OriginMesh.name,asset.OriginMesh.extension = os.path.splitext(os.path.basename(asset.OriginMesh.uri))
                        else:
                            lod = ut.LOD()
                            lod.mesh = ut.AssetMesh()
                            lod.mesh.uri = mesh_uri
                            lod.mesh.name,lod.mesh.extension = os.path.splitext(os.path.basename(lod.mesh.uri))
                            lod.level = ut.GetLodLevelByName(lod.mesh.name)-1  #此处使用lod0作为原始模型,所以其余的lod需要减一
                            asset.Lods.append(lod)
                    else:
                        current_variation_index = mesh['variation']-1 #此处使用变体0作为原始模型,所以其余的变体需要减一
                        if current_variation_index in meshVars.keys():
                            meshVar = meshVars[current_variation_index]
                        else:
                            meshVar = ut.MeshVar()
                            meshVars[current_variation_index] = meshVar
                        if mesh['lod'] == 0:
                            meshVar.OriginMesh = ut.AssetMesh()
                            meshVar.OriginMesh.uri = mesh_uri
                            meshVar.OriginMesh.name,meshVar.OriginMesh.extension = os.path.splitext(os.path.basename(meshVar.OriginMesh.uri))
                            meshVar.VarIndex = current_variation_index
                        else:
                            lod = ut.LOD()
                            lod.mesh = ut.AssetMesh()
                            lod.mesh.uri = mesh_uri
                            lod.mesh.name,lod.mesh.extension = os.path.splitext(os.path.basename(lod.mesh.uri))
                            lod.level = ut.GetLodLevelByName(lod.mesh.name)-1  #此处使用lod0作为原始模型,所以其余的lod需要减一
                            meshVar.Lods.append(lod)
                        meshVars[current_variation_index] = meshVar
                asset.MeshVars = [var for var in meshVars.values()]
                asset.MeshVars = sorted(asset.MeshVars,key=lambda meshvar:meshvar.VarIndex)
                for i in range(len(asset.MeshVars)):
                    asset.MeshVars[i].Lods = sorted(asset.MeshVars[i].Lods,key=lambda lod:lod.level)
                asset.Lods = sorted(asset.Lods ,key=lambda lod:lod.level)
            else:
                raise
        elif asset.type == ut.AssetType.Surface or asset.type == ut.AssetType.Decal:
            asset.surfaceSize = ut.AssetSize.Meter1
        else:
            pass
        preview_uri = os.path.join(assetRootPath,assetData['preview'][-1])
        if not os.path.exists(preview_uri):
            raise
        asset.previewFile.append(preview_uri)
        asset.assetFormat = ut.AssetFormat.FBX
        asset.OldJson = jsPath

        new_tags = []
        for tag in asset.tags:
            try:
                new_tag = tagsTranslation[tag].strip()
                if new_tag!= "":
                    new_tags.append(new_tag)
            except:
                pass
            try:
                new_tag = tag_category[tag].strip()
                if new_tag!= "":
                    new_tags.append(new_tag)
            except:
                pass
        new_tags = set(new_tags)
        asset.tags = list(new_tags)
        for tag in asset.tags:
            if tag in ut.GetCategorys(1):
                asset.subcategory = tag
                for p in ut.GetCategorys(0):
                    if tag in ut.category[p]:
                        asset.category = p
                break
            else:
                categorys = ut.GetParentsCategory(tag)
                if categorys: 
                    asset.category = categorys[0]
                    asset.subcategory = categorys[1]
                    break
